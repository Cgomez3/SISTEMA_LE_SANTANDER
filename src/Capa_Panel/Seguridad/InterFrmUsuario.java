    private void btnguardaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardaActionPerformed
        // TODO add your handling code here:
        if(validar()){
            EUsuario objusu=new EUsuario();
            objusu.setDNI(txtdni.getText().trim());
            objusu.setNombres(txtnombres.getText().toUpperCase().trim());
            objusu.setApePaterno(txtapepat.getText().toUpperCase().trim());
            objusu.setApeMaterno(txtapemat.getText().toUpperCase().trim());
            objusu.setEmail(txtemail.getText().trim());
            objusu.setEstado((chkestado.isSelected()) ? "ACTIVO" : "INACTIVO");
            objusu.setContra(txtcontra.getText().trim());
            objusu.setImportar((chklibroselectronico.isSelected()) ? "SI" : "NO");
            objusu.setExportar((chkprogpagos.isSelected()) ? "SI" : "NO");
            objusu.setSeguridad((chkseguridad.isSelected()) ? "SI" : "NO");
            LUsuario lusu = new LUsuario();
            if(accion==0){
                if(verificardni(objusu.getDNI())){
                    System.out.println(nomusuario());
                    objusu.setUsuario(nomusuario());
                    if (JOptionPane.showConfirmDialog(null, "Se va GRABAR el Usuario " + objusu.getUsuario() + ". ¿Desea Continuar?", "Confirmar", JOptionPane.YES_NO_OPTION) == (JOptionPane.YES_OPTION)) {
                        if(lusu.InsertarUsuario(objusu)){
                            JOptionPane.showMessageDialog(this, "Se grabó correctamente al usuario " + objusu.getUsuario() + ".");
                            limpiar();
                            llenartablausuario();
                        }else{
                            JOptionPane.showMessageDialog(this, "Error de tipo de Dato y/o Conexión de BD.");
                        }
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "DNI ingresado ya se encuentra registrado.");
                    txtdni.requestFocus();
                }
            }else{
                objusu.setUsuario(txtusuario.getText().toUpperCase().trim());
                if (JOptionPane.showConfirmDialog(null, "Se va MODIFICAR el Usuario " + objusu.getUsuario() + ". ¿Desea Continuar?", "Confirmar", JOptionPane.YES_NO_OPTION) == (JOptionPane.YES_OPTION)) {
                    if (lusu.ActualizarUsuario(objusu)) {
                        JOptionPane.showMessageDialog(this, "Se actualizó correctamente al usuario " + objusu.getUsuario() + ".");
                        limpiar();
                        llenartablausuario();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error de tipo de Dato y/o Conexión de BD.");
                    }
                }
            }
        }
    }//GEN-LAST:event_btnguardaActionPerformed

