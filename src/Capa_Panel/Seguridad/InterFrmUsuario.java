    private void llenartablausuario() {
        LUsuario lusu = new LUsuario();
        lstusuario = lusu.ListaUsuario();
        modTbdatos.setRowCount(0);
        if (lstusuario.size() > 0) {
            for (int i = 0; i < lstusuario.size(); i++) {
                Object obj[] = new Object[11];
                obj[0] = lstusuario.get(i).getDNI();
                obj[1] = lstusuario.get(i).getNombres();
                obj[2] = lstusuario.get(i).getApePaterno();
                obj[3] = lstusuario.get(i).getApeMaterno();
                obj[4] = lstusuario.get(i).getUsuario();
                obj[5] = lstusuario.get(i).getContra();
                obj[6] = lstusuario.get(i).getEstado();
                obj[7] = lstusuario.get(i).getEmail();
                obj[8] = lstusuario.get(i).getImportar();
                obj[9] = lstusuario.get(i).getExportar();
                obj[10] = lstusuario.get(i).getSeguridad();
                modTbdatos.addRow(obj);
            }
        }
        tblstusuario.setModel(modTbdatos);
        lblcont.setText(tblstusuario.getRowCount() + " Usuario(s)");
    }
    
    private String nomusuario() {
        StringBuilder sbr = new StringBuilder();
        sbr.append(txtnombres.getText().toString().trim().substring(0, 1));
        sbr.append(txtapepat.getText().toString().trim());
        for (int i = 0; i < lstusuario.size(); i++) {
            if (sbr.toString().equalsIgnoreCase(lstusuario.get(i).getUsuario())) {
                sbr.append(txtapemat.getText().toString().trim());
            }
        }
        return sbr.toString().toUpperCase();
    }
    
    private boolean validar() {
        int val = 0;
        if (txtdni.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Ingrese DNI.");
            txtdni.requestFocus();
        } else {
            if (txtdni.getText().trim().length() < 8) {
                JOptionPane.showMessageDialog(this, "DNI debe tener 8 caracteres.");
                txtdni.requestFocus();
            } else {
                if (txtnombres.getText().trim().length() == 0) {
                    JOptionPane.showMessageDialog(this, "Ingrese Nombres.");
                    txtnombres.requestFocus();
                } else {
                    if (txtapepat.getText().trim().length() == 0) {
                        JOptionPane.showMessageDialog(this, "Ingrese Apellido Paterno.");
                        txtapepat.requestFocus();
                    } else {
                        if (txtapemat.getText().trim().length() == 0) {
                            JOptionPane.showMessageDialog(this, "Ingrese Apellido Materno.");
                            txtapemat.requestFocus();
                        } else {
                            if (txtcontra.getText().trim().length() == 0) {
                                JOptionPane.showMessageDialog(this, "Ingrese Contraseña.");
                                txtcontra.requestFocus();
                            } else {
                                if (txtcontra.getText().trim().length() < 6) {
                                    JOptionPane.showMessageDialog(this, "Contraseña debe tener un mínimo de 6 caracteres.");
                                    txtcontra.requestFocus();
                                } else {
                                    if (!chklibroselectronico.isSelected() 
                                            && !chkprogpagos.isSelected() 
                                            && !chkseguridad.isSelected()) {
                                        JOptionPane.showMessageDialog(this, "Seleccione al menos uno de los accesos disponibles.");
                                    } else {
                                        val = 1;
                                    }

                                }
                            }
                        }
                    }
                }
            }
        }
        if (val == 1) {
            return true;
        } else {
            return false;
        }
    }
    
    private boolean verificardni(String dni) {
        int rep = 0;
        for (int i = 0; i < lstusuario.size(); i++) {
            if (lstusuario.get(i).getDNI().equalsIgnoreCase(dni)) {
                rep = 1;
            }
        }
        if (rep == 0) {
            return true;
        } else {
            return false;
        }
    }
    
    private void seleccionfila(int fila){
        txtdni.setText(tblstusuario.getValueAt(fila, 0).toString());
        txtnombres.setText(tblstusuario.getValueAt(fila, 1).toString());
        txtapepat.setText(tblstusuario.getValueAt(fila, 2).toString());
        txtapemat.setText(tblstusuario.getValueAt(fila, 3).toString());
        txtusuario.setText(tblstusuario.getValueAt(fila, 4).toString());
        txtcontra.setText(tblstusuario.getValueAt(fila, 5).toString());
        if (tblstusuario.getValueAt(fila, 6).toString().equalsIgnoreCase("ACTIVO")) {
            chkestado.setSelected(true);
        } else {
            chkestado.setSelected(false);
        }
        txtemail.setText(tblstusuario.getValueAt(fila, 7).toString().trim());
        marcarchecks(chklibroselectronico, tblstusuario.getValueAt(fila, 8).toString().trim());
        marcarchecks(chkprogpagos, tblstusuario.getValueAt(fila, 9).toString().trim());
        marcarchecks(chkseguridad, tblstusuario.getValueAt(fila, 10).toString().trim());
        accion=1;
        txtdni.setEditable(false);
    }
    
    private void marcarchecks(JCheckBox chk, String estado){
        if (estado.equalsIgnoreCase("SI")) {
            chk.setSelected(true);
        } else {
            chk.setSelected(false);
        }
    }
    
