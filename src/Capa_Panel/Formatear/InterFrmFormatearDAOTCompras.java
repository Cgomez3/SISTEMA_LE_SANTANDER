    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        javax.swing.JFileChooser jF1 = new javax.swing.JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("XLS files", "xls");
        jF1.setFileFilter(filtro);
        String ruta = "";
        try {
            if (jF1.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {
                ruta = jF1.getSelectedFile().getAbsolutePath();
                System.out.println(ruta);
                int val=0;
                WorkbookSettings wbSettings = new WorkbookSettings();
                wbSettings.setEncoding("ISO-8859-1");
                wbSettings.setLocale(new Locale("es", "ES"));
                wbSettings.setExcelDisplayLanguage("ES");
                wbSettings.setExcelRegionalSettings("ES");
                wbSettings.setCharacterSet(CountryCode.SPAIN.getValue());
                Workbook workbook = Workbook.getWorkbook(new File(ruta), wbSettings);
                lstdaot.removeAll(lstdaot);
                Sheet sheet = workbook.getSheet(0);
                System.out.println(sheet.getCell(0, 4).getContents().trim());
                if(!esNumeroEntero(sheet.getCell(0, 4).getContents().trim())){   
                    val=1;
                    JOptionPane.showMessageDialog(this, "El archivo que intenta cargar no cuenta con el formato adecuado.");
                }
                int cont=0;
                for (int fila = 4; fila < sheet.getRows(); fila++) {
                    //recorremos las filas
                    cont=cont+1;
                    if(sheet.getCell(0, fila).getContents().toString().trim().length()>0){
                        EFormatoDAOTVentas objdaot=new EFormatoDAOTVentas();
                        if (val == 1) {
                            break;
                        }
                        if (!esNumeroEntero(sheet.getCell(0, fila).getContents().trim())) {
                            System.out.println("ERROR: " + sheet.getCell(0, fila).getContents());
                        } else {
                            objdaot.setFila(cont);
                            objdaot.setTipoDocSantander("6");
                            objdaot.setRucSantander("20550226589");
                            objdaot.setAño(2015);
                            objdaot.setTipoPersona(sheet.getCell(0, fila).getContents());
                            objdaot.setTipoDocCliente(sheet.getCell(1, fila).getContents());
                            objdaot.setRucCliente(sheet.getCell(2, fila).getContents().trim());
                            if (objdaot.getTipoPersona().trim().equalsIgnoreCase("02")) {
                                objdaot.setRazonSocial(sheet.getCell(3, fila).getContents().trim().toUpperCase());
                                objdaot.setNombresSinSeparar("|||");
                            } else {
                                objdaot.setRazonSocial("");
                                objdaot.setNombresSinSeparar(sheet.getCell(3, fila).getContents().toUpperCase().trim().replace(" ", "|"));
                            }
                            objdaot.setMonto(Integer.valueOf(sheet.getCell(4, fila).getContents().trim()));
                            lstdaot.add(objdaot);
                        }
                    }
                }
                llenartabla();
            }
        } catch (HeadlessException | IOException | BiffException | IndexOutOfBoundsException ex) {
            JOptionPane.showMessageDialog(this, "El archivo que intenta cargar no cuenta con el formato adecuado.");
            System.out.println(ex);
        }
        
        llenartabla();
    }//GEN-LAST:event_jButton1ActionPerformed

