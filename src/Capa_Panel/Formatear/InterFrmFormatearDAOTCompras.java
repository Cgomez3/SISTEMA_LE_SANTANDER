    private boolean Contar3palotes(String nombres) {
        char[] arrayChar = nombres.toCharArray();
        int contador = 0;
        for (int i = 0; i < arrayChar.length; i++) {
            if (arrayChar[i] == '|') {
                contador = contador + 1;
            }
        }
        if(contador==3){
            return true;
        }else{
            return false;
        }
    }

    
    public boolean esNumeroEntero(String cad) {
        try {
            Integer.parseInt(cad);
            return true;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }
    
