public class EParametrosRetorno {

    /**
     * @return the nroDiario
     */
    public String getNroDiario() {
        return nroDiario;
    }

    /**
     * @param nroDiario the nroDiario to set
     */
    public void setNroDiario(String nroDiario) {
        this.nroDiario = nroDiario;
    }

    /**
     * @return the nroDocumento
     */
    public String getNroDocumento() {
        return nroDocumento;
    }

    /**
     * @param nroDocumento the nroDocumento to set
     */
    public void setNroDocumento(String nroDocumento) {
        this.nroDocumento = nroDocumento;
    }

    /**
     * @return the ruc
     */
    public String getRuc() {
        return ruc;
    }

    /**
     * @param ruc the ruc to set
     */
    public void setRuc(String ruc) {
        this.ruc = ruc;
    }

    /**
     * @return the nombreProveedor
     */
    public String getNombreProveedor() {
        return nombreProveedor;
    }

    /**
     * @param nombreProveedor the nombreProveedor to set
     */
    public void setNombreProveedor(String nombreProveedor) {
        this.nombreProveedor = nombreProveedor;
    }

    /**
     * @return the modeda
     */
    public String getModeda() {
        return modeda;
    }

    /**
     * @param modeda the modeda to set
     */
    public void setModeda(String modeda) {
        this.modeda = modeda;
    }
    private String nroDiario;
    private String nroDocumento;
    private String ruc;
    private String nombreProveedor;
    private String modeda;
    
}
