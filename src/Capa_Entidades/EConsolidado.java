public class EConsolidado {
    private int CodProgramacion;
    private String DocProv;
    private String RazonSocial;
    private double montosoles;
    private double montodolares;
    private String TipoComprob;

    /**
     * @return the CodProgramacion
     */
    public int getCodProgramacion() {
        return CodProgramacion;
    }

    /**
     * @param CodProgramacion the CodProgramacion to set
     */
    public void setCodProgramacion(int CodProgramacion) {
        this.CodProgramacion = CodProgramacion;
    }

    /**
     * @return the DocProv
     */
    public String getDocProv() {
        return DocProv;
    }

    /**
     * @param DocProv the DocProv to set
     */
    public void setDocProv(String DocProv) {
        this.DocProv = DocProv;
    }

    /**
     * @return the RazonSocial
     */
    public String getRazonSocial() {
        return RazonSocial;
    }

    /**
     * @param RazonSocial the RazonSocial to set
     */
    public void setRazonSocial(String RazonSocial) {
        this.RazonSocial = RazonSocial;
    }

    /**
     * @return the montosoles
     */
    public double getMontosoles() {
        return montosoles;
    }

    /**
     * @param montosoles the montosoles to set
     */
    public void setMontosoles(double montosoles) {
        this.montosoles = montosoles;
    }

    /**
     * @return the montodolares
     */
    public double getMontodolares() {
        return montodolares;
    }

    /**
     * @param montodolares the montodolares to set
     */
    public void setMontodolares(double montodolares) {
        this.montodolares = montodolares;
    }

    /**
     * @return the TipoComprob
     */
    public String getTipoComprob() {
        return TipoComprob;
    }

    /**
     * @param TipoComprob the TipoComprob to set
     */
    public void setTipoComprob(String TipoComprob) {
        this.TipoComprob = TipoComprob;
    }
}
